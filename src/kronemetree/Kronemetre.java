    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
       try{
           drm=true;
          Timer timer = new Timer(); 
          TimerTask task = new TimerTask() {
            @Override
            public void run() {
                if (drm==false) {
                   timer.cancel();
                }
                sny++;
                txtsny.setText(Integer.toString(sny));
                if (sny==60) {
                    dkka++;
                    txtdkk.setText(Integer.toString(dkka));
                    sny=0;
                    if(dkka==60){
                        Saat++;
                        txtsaat.setText(Integer.toString(Saat));
                        dkka=0;
                    }
                    if(Saat==60){
                        Aslsaat++;
                        txtsaat2.setText(Integer.toString(Aslsaat));
                        Saat=0;
                    }
                }
            }
        };
        timer.scheduleAtFixedRate(task, 1000, 10); 
     }
     catch(Exception ex)
     {
     JOptionPane.showMessageDialog(null, ex.getMessage() ,"Hata", 1);
     }
    }//GEN-LAST:event_jButton1MouseClicked

